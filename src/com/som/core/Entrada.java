package com.som.core;

public class Entrada {
	
	private String nome;
	private int[] pesos;
	
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int[] getPesos() {
		return pesos;
	}
	public void setPesos(int[] pesos) {
		this.pesos = pesos;
	}

}
